// Задача 3. Дано масив імен учнів. З’ясувати скільки разів зустрічається ім’я «Іван».

void main()
{
  List<String> names = 
  [
    "Іван",
    "Ксюша",
    "Міша",
    "Іван",
    "Артур",
    "Саша",
    "Андрій",
    "Іван",
    "Софія"
  ];

  int count = names.where((name) => name == "Іван").length;

  print("Ім'я Іван зустрічається $count разів.");
}